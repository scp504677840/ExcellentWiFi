package com.jzlg.excellentwifi.entity;

public class WifiPoint {
	
	private int x;
	
	private int y;
	
	public WifiPoint(int x1,int y1){
		x=x1;
		y=y1;
	}
	

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	

}
